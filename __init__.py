# Pour rappel, ce fichier permet d'indiquer que le répertoire courant
# est un module Python. C'est grace à la présence de ce fichier qu'il
# est possible de faire "import mini_url.models", par exemple.
# En revanche, il est impossible de faire "import mini_url.templates"
# puisque le dossier templates n'a pas de __init__.py.
#
# Concrètement, nous n'avons rien à faire dans ce fichier. Il arrive
# cependant que certaines applications Django contiennent du code dans
# leur fichiers __init__, pour des raisons de simplifications. Nous
# n'aborderons pas plus en détail ce point.
